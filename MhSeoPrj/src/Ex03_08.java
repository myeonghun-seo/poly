public class Ex03_08 
{
	public static void main(String[] args)
	{
		int a, b;			// 정수형 변수가 2개이다.
		float c, d;			// 실수형 변수가 2개이다.
		
		a = 100;			// a에 정수 100을 대입한다.
		b = a;				// b에 a값을 대입한다.
		
		c = 111.1f;			// c에 실수 111.1을 대입한다.
		d = c;				// d에 c값을 대입한다.

		System.out.println("a, b의 값 ==>" + a + ", " + b);
		System.out.println("c, d의 값 ==>" + c + ", " + d);
	}
	
}
